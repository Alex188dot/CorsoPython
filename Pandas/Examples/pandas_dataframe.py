'''esempio di dataframes'''
import pandas
import pandas as pd

data = {
  "calories": [420, 380, 390],
  "duration": [50, 40, 45]
}

myvar = pd.DataFrame(data)

print(myvar)

print(myvar.loc[2])

print("-----------------------------------------")

print("Same dataframe but with index")

data = {
  "calories": [420, 380, 390],
  "duration": [50, 40, 45]
}

df = pd.DataFrame(data, index = ["day1", "day2", "day3"])

print(df)

print(df.loc["day1"])

print("-----------------------------------------")


